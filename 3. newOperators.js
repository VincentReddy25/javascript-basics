// Strict Operators ( ===  !== )

let num1 = 10; // number
let num2 = "10"; // string

num1 == num2 ? console.log("true") : console.log("false");
// true
// Checking Value 

num1 === num2 ? console.log("true") : console.log("false");
// false
// Checking Value + Data type


// Exponent operator
console.log(2 * 3); // 6
console.log(2 ** 3); // 8

